//Account class with name instance variable

public class Account 
{
    
//Instance variable    
    private String name;
    
//Set method for object    
    public void setName(String name)
    {
        this.name = name;
    }
    
//Get method that returns object    
    public String getName()
    {
        return name;
    }
    
}
