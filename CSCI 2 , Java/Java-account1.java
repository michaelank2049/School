//Account class with constructor

public class Account1 
{
    private String name;
    
//Constructor initializes name     
    public Account1(String name)
    {
        this.name = name;
    }
 
//Method to set name    
    public void setName(String name)
    {
        this.name = name;
    }
   
//Method to get name    
    public String getName()
    {
        return name;
    }
    
}
